function updateLanguage(lang) {
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = translations[lang][key];
            } else {
                element.innerHTML = translations[lang][key];
            }
        }
    });

    // Update HTML lang attribute
    document.documentElement.lang = lang;

    // Current lang label is now hidden (Flag only mode)

    const currentLangFlag = document.getElementById('current-lang-flag');
    const flags = {
        'en': 'fi-us',
        'fr': 'fi-fr',
        'la': 'fi-la',
        'ja': 'fi-jp',
        'th': 'fi-th'
    };

    if (currentLangFlag) {
        // Remove existing fi-* classes and add the new one
        currentLangFlag.className = 'fi ' + (flags[lang] || '');
    }

    // Save preference
    localStorage.setItem('preferred_language', lang);

    // Update active state of language buttons
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
    });

    // Body font class for Lao
    if (lang === 'la') {
        document.body.classList.add('lao-font');
    } else {
        document.body.classList.remove('lao-font');
    }
}

document.addEventListener('DOMContentLoaded', () => {
    // Check for saved language or use browser default
    const savedLang = localStorage.getItem('preferred_language');
    const browserLang = navigator.language.split('-')[0];
    const defaultLang = (translations[savedLang]) ? savedLang : (translations[browserLang] ? browserLang : 'en');

    updateLanguage(defaultLang);

    // Single delegated listener for language buttons
    document.addEventListener('click', (e) => {
        const langBtn = e.target.closest('.lang-btn');
        if (langBtn) {
            const lang = langBtn.getAttribute('data-lang');
            updateLanguage(lang);

            // Force-hide the dropdown to prevent hover sticky issues on mobile and desktop
            const langDropdown = document.querySelector('.lang-dropdown');
            if (langDropdown) {
                langDropdown.classList.remove('active');
                langDropdown.classList.add('force-hidden');

                // Allow it to be opened again after a short delay
                setTimeout(() => {
                    langDropdown.classList.remove('force-hidden');
                }, 500);
            }
        }
    });

    // Also close mobile nav when a regular link is clicked
    document.querySelectorAll('.main-nav a').forEach(link => {
        link.addEventListener('click', (e) => {
            // Do not close nav if we're clicking a dropdown toggle on mobile
            if (window.innerWidth <= 991 && (link.parentElement.classList.contains('has-dropdown') || link.parentElement.classList.contains('has-subdropdown'))) {
                return;
            }

            const nav = document.getElementById('main-nav');
            const menuToggle = document.getElementById('mobile-menu-btn');
            const overlay = document.querySelector('.nav-overlay');
            if (nav && nav.classList.contains('active')) {
                nav.classList.remove('active');
                menuToggle.classList.remove('active');
                if (overlay) overlay.classList.remove('active');
                document.body.style.overflow = '';
            }
        });
    });
});

